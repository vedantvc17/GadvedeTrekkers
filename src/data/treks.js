export function slugifyTrekName(name) {
  return name.toLowerCase().replace(/[^a-z0-9]+/g, "-");
}

export function findTrekBySlug(slug) {
  return uniqueTreks.find((trek) => slugifyTrekName(trek.name) === slug) ?? null;
}
